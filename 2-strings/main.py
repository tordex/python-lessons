# Конкатенация строк
print("String1" + "-" + "String2")

# Форматирование строк
print("integer: {:04}, hex: {hex:04X}, string: {named}".format(10, hex=34589, named="yes"))

# Escape последовательности
print("line1\n\tline2")

# raw строки
print(r"line1\n\tline2")

# Многострочные строки
print("""Line1
Something this
"must be"
\thello
""")
